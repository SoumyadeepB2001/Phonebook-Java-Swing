    private void delButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delButActionPerformed

       // TODO add your handling code here:
       
       if(jTable1.getSelectedRow() == -1)
        {
            JOptionPane.showMessageDialog(null,"No contact selected.");  
        }
       else{
        int YesOrNo = JOptionPane.showConfirmDialog(null,"Do you want to remove this contact?","Remove Contact", JOptionPane.YES_NO_OPTION);
        if(YesOrNo == 0)
        {
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        int rowindex =jTable1.getSelectedRow();
        int modelRow =jTable1.convertRowIndexToModel(rowindex);
        model.removeRow(modelRow);
        }
       }
    }//GEN-LAST:event_delButActionPerformed

    private void saveButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButActionPerformed

        filter(0);
        File file = new File("Contacts.txt");
        try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            for(int i = 0; i < jTable1.getRowCount(); i++){//rows
                for(int j = 0; j < jTable1.getColumnCount(); j++){//columns
                    if(jTable1.getValueAt(i, j) == null)
                    {
                        bw.write(""+"¶");
                    }
                    else{
                    bw.write(String.valueOf(jTable1.getValueAt(i,j)));
                    bw.write("¶");
                    
                    }
                }
                bw.newLine();
            }
            
            bw.close();
            fw.close();
            tfname.setText("");
            tfnum.setText("");
            tfadd.setText("");
            tfeid.setText("");
        } catch (IOException ex) {
            System.out.println("exception");
            java.util.logging.Logger.getLogger(JFrame1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveButActionPerformed

